#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (c == '\0')
	{
		s += ft_strlen((char *)s);
		return ((char *)s);
	}
	while (*s)
	{
		if (*s == (char)c)
			return ((char *)s);
		s++;
	}
	return (NULL);
}

#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	uint8_t	*str1;
	uint8_t	*str2;

	str1 = (uint8_t *)s1;
	str2 = (uint8_t *)s2;
	while ((*str1 || *str2) && n--)
	{
		if (*str1 != *str2)
			return (*str1 - *str2);
		str1++;
		str2++;
	}
	return (0);
}
